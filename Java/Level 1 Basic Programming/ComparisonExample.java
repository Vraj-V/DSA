// Question: What are comparison operators in Java?
// Answer: They compare two values (==, !=, >, <, >=, <=).

public class ComparisonExample {
    public static void main(String[] args) {
        int a = 10, b = 20;

        System.out.println("a == b : " + (a == b));
        System.out.println("a != b : " + (a != b));
        System.out.println("a > b : " + (a > b));
        System.out.println("a < b : " + (a < b));
    }
}
